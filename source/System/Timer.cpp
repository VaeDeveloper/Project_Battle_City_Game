#include "Timer.h"

//----------------------------------------------------------------------------------------------------------------------------------------------------------------
Timer::Timer()
	:bRunning(false),Time_Left(0)
{
}
//----------------------------------------------------------------------------------------------------------------------------------------------------------------
void Timer::Update(const double delta)
{
	if (bRunning)
	{
		Time_Left -= delta;

		if (Time_Left <= 0)
		{
			bRunning = false;
			Callback();
		}
	}
}
//----------------------------------------------------------------------------------------------------------------------------------------------------------------
void Timer::Start_Timer(const double duration)
{
	Time_Left = duration;
	bRunning = true;
}
//----------------------------------------------------------------------------------------------------------------------------------------------------------------
void Timer::Set_Callback(std::function<void()> callback)
{
	Callback = callback;
}
