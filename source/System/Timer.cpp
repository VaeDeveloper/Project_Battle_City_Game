#include "Timer.h"

//----------------------------------------------------------------------------------------------------------------------------------------------------------------
Timer::Timer() : bIsRunning(false), Time_Left(0) {}
//----------------------------------------------------------------------------------------------------------------------------------------------------------------
void Timer::Update(const double delta)
{
    if (bIsRunning)
    {
        Time_Left -= delta;

        if (Time_Left <= 0)
        {
            bIsRunning = false;
            Callback();
        }
    }
}
//----------------------------------------------------------------------------------------------------------------------------------------------------------------
void Timer::Start_Timer(const double duration)
{
    Time_Left = duration;
    bIsRunning = true;
}
//----------------------------------------------------------------------------------------------------------------------------------------------------------------
void Timer::Set_Callback(std::function<void()> callback)
{
    Callback = callback;
}
